package actividad3pt1;

public class PracticaTipusDades1 {

    public static void main(String[] args) {

        // Declaramos las variables y las calculamos como lo dice el enunciado.
        int valorInt = 35 + 2; // Declaramos el valorInt y le sumamos 2 unidades.

        System.out.println("Total: " + valorInt); // Imprimimos el valor.

        // Declaramos valorString y le damos el valor de cadena "Aprenentatge".
        String valorString = "Aprenentatge";
        System.out.println("String: " + valorString); // Imprimimos el valor.

        double valorDouble = 45.98 + 1.5; // Declaramos valorDouble y le sumamos 1.5 unidades.
        System.out.println("Total: " + valorDouble); // Imprimimos el valor.

    }

}