package activitats.biblioteca;

import java.io.*;

public class Material implements Serializable {

    private int id;
    private String titol;

    // Getters i setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitol() {
        return titol;
    }

    public void setTitol(String titol) {
        this.titol = titol;
    }
}
